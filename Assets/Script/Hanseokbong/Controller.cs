    public static string WriteHangle(string sentence)
    {
        string result = "";
        StringBuilder tempResult = new StringBuilder();
        string[] word = sentence.Split(' ');
        List<char> cJaso; //우리가 입력한 자음 및 모음이 다 저장
        List<int> bJaso; //자음과 모음의 구분: 자음 - 1, 모음 - 0, space - 2
        for (int count = 0; count < word.Length; count++)
        {
            int n = word[count].Length;
            cJaso = new List<char>();
            bJaso = new List<int>();
            //자음 및 모음 구분(space 구분 포함)
            for (int i = 0; i < n; i++)
            {
                cJaso.Add(word[count][i]);
                for (int j = 0; j < choSung.Length; j++)
                {
                    if (choSung[j] == word[count][i])
                    {
                        bJaso.Add(1);
                        break;
                    }
                }

                if (cJaso.Count != bJaso.Count)
                {
                    if (jongSung[0] == word[count][i])
                    {
                        bJaso.Add(2);
                    }
                    else
                    {
                        bJaso.Add(0);
                    }
                }
            }

            for (int j = 0; j < 5; j++)
            {
                cJaso.Add(' ');
                bJaso.Add(3);
            }

            int index = 0;
            const int SIZE = 6;
            for (int i = 0; i < cJaso.Count; i++)
            {
                index++;
                if (index == SIZE)
                {
                    i -= SIZE - 1; index = 0;

                    //0
                    if (bJaso[i] == 0)
                    {
                        tempResult.Append(cJaso[i]);
                        index = 0;
                    }
                    //11
                    else if (bJaso[i + 1] == 1)
                    {
                        tempResult.Append(cJaso[i]);
                        index = 0;
                    }
                    //예외 상황
                    else if (i == n - 2)
                    {
                        tempResult.Append(hangleHap(cJaso[i], cJaso[i + 1], ' '));
                        i++;
                    }
                    //100
                    else if (bJaso[i + 2] == 0)
                    {
                        char shortVowel = JudgeShortVowel(cJaso[i + 1], cJaso[i + 2]);

                        //단모음
                        if (shortVowel == '?')
                        {
                            tempResult.Append(hangleHap(cJaso[i], cJaso[i + 1], ' '));
                            index = 1;
                        }
                        //장모음
                        else
                        {
                            //1000
                            if (bJaso[i + 3] == 0)
                            {
                                tempResult.Append(hangleHap(cJaso[i], shortVowel, ' '));
                                index = 2;
                            }
                            else
                            {
                                //10010
                                if (bJaso[i + 4] == 0)
                                {
                                    tempResult.Append(hangleHap(cJaso[i], shortVowel, ' '));
                                    index = 2;
                                }
                                else
                                {
                                    char shortConsonant = JudgeShortConsonant(cJaso[i + 3], cJaso[i + 4]);

                                    //홑받침
                                    if (shortConsonant == '?')
                                    {
                                        tempResult.Append(hangleHap(cJaso[i], shortVowel, cJaso[i + 3]));
                                        index = 3;
                                    }
                                    //겹받침
                                    else
                                    {
                                        //100111
                                        if (bJaso[i + 5] == 1)
                                        {
                                            tempResult.Append(hangleHap(cJaso[i], shortVowel, shortConsonant));
                                            index = 4;
                                        }
                                        //100110
                                        else
                                        {
                                            tempResult.Append(hangleHap(cJaso[i], shortVowel, cJaso[i + 3]));
                                            index = 3;
                                        }
                                    }
                                }
                            }
                        }
                    }
                    //101
                    else if (bJaso[i + 2] == 1)
                    {
                        //1010
                        if (bJaso[i + 3] == 0)
                        {
                            tempResult.Append(hangleHap(cJaso[i], cJaso[i + 1], ' '));
                            index = 1;
                        }
                        //1011
                        else
                        {
                            char shortConsonant = JudgeShortConsonant(cJaso[i + 2], cJaso[i + 3]);

                            //홑받침
                            if (shortConsonant == '?')
                            {
                                tempResult.Append(hangleHap(cJaso[i], cJaso[i + 1], cJaso[i + 2]));
                                index = 2;
                            }
                            //겹받침
                            else
                            {
                                //10110
                                if (bJaso[i + 4] == 0)
                                {
                                    tempResult.Append(hangleHap(cJaso[i], cJaso[i + 1], cJaso[i + 2]));
                                    index = 2;
                                }
                                //10111
                                else
                                {
                                    tempResult.Append(hangleHap(cJaso[i], cJaso[i + 1], shortConsonant));
                                    index = 3;
                                }
                            }
                        }
                    }

                    i += index;
                    index = 0;
                }
            }

            tempResult.Append(" ");
        }

        tempResult.Remove(tempResult.Length - 1, 1);
        result = tempResult.ToString();

        return result;
    }

    //단모음 판별
    private static char JudgeShortVowel(char front, char back)
    {
        //단모음 종류 : ㅘ,ㅙ,ㅚ,ㅝ,ㅞ,ㅟ,ㅢ
        char result = '?';
        if (front == 'ㅗ' && back == 'ㅏ')
        {
            result = 'ㅘ';
        }
        else if (front == 'ㅗ' && back == 'ㅐ')
        {
            result = 'ㅙ';
        }
        else if (front == 'ㅗ' && back == 'ㅣ')
        {
            result = 'ㅚ';
        }
        else if (front == 'ㅜ' && back == 'ㅓ')
        {
            result = 'ㅝ';
        }
        else if (front == 'ㅜ' && back == 'ㅔ')
        {
            result = 'ㅞ';
        }
        else if (front == 'ㅜ' && back == 'ㅣ')
        {
            result = 'ㅟ';
        }
        else if (front == 'ㅡ' && back == 'ㅣ')
        {
            return 'ㅢ';
        }

        return result;
    }

    //단자음 판별
    private static char JudgeShortConsonant(char front, char back)
    {
        //단자음 종류 : ㄳ, ㄵ, ㄶ, ㄺ, ㄻ, ㄼ, ㄽ, ㄾ, ㄿ, ㅀ, ㅄ
        char result = '?';
        if (front == 'ㄱ' && back == 'ㅅ')
        {
            result = 'ㄳ';
        }
        else if (front == 'ㄴ' && back == 'ㅈ')
        {
            result = 'ㄵ';
        }
        else if (front == 'ㄴ' && back == 'ㅎ')
        {
            result = 'ㄶ';
        }
        else if (front == 'ㄹ' && back == 'ㄱ')
        {
            result = 'ㄺ';
        }
        else if (front == 'ㄹ' && back == 'ㅁ')
        {
            result = 'ㄻ';
        }
        else if (front == 'ㄹ' && back == 'ㅂ')
        {
            result = 'ㄼ';
        }
        else if (front == 'ㄹ' && back == 'ㅅ')
        {
            result = 'ㄽ';
        }
        else if (front == 'ㄹ' && back == 'ㅌ')
        {
            result = 'ㄾ';
        }
        else if (front == 'ㄹ' && back == 'ㅍ')
        {
            result = 'ㄿ';
        }
        else if (front == 'ㄹ' && back == 'ㅎ')
        {
            result = 'ㅀ';
        }
        else if (front == 'ㅂ' && back == 'ㅅ')
        {
            result = 'ㅄ';
        }

        return result;
    }

    private static char hangleHap(char c1, char c2, char c3)  // 초성, 중성은 무조건 와야하며, 종성이 없으면 ' '을 입력해야함
    {
        int cho_i = 0, jung_i = 0, jong_i = 0;

        for (int i = 0; i < choSung.Length; i++)
        {
            if (choSung[i] == c1)
            {
                cho_i = i;
            }
        }

        for (int i = 0; i < jungSung.Length; i++)
        {
            if (jungSung[i] == c2)
            {
                jung_i = i;
            }
        }

        for (int i = 0; i < jongSung.Length; i++)
        {
            if (jongSung[i] == c3)
            {
                jong_i = i;
            }
        }

        int uniValue = (cho_i * 21 * 28) + (jung_i * 28) + jong_i + 0xAC00;

        return (char)uniValue;
    }
}

