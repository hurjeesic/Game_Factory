public class Controller : MonoBehaviour
{
    public static List<string> Japan;
    public static bool bGame, bComplete;
    public static int level;
    private int score;
    GameObject offerObj, resultObj, rightObj, wrongObj;

    void Awake()
    {
        bGame = false;
        score = 0;
        Japan = Japan = new List<string>();
        level = 2; //임시
        bComplete = false;
        GameTime.Play = 60;
    }

    // Start is called before the first frame update
    void Start()
    {
        offerObj = GameObject.Find("Offer");
        resultObj = GameObject.Find("Result");
        rightObj = GameObject.Find("Right");
        wrongObj = GameObject.Find("Wrong");

        offerObj.GetComponent<Text>().text = "";
        GameObject.Find("Offer Title").GetComponent<Text>().text = "";
        resultObj.GetComponent<Text>().text = "";
        GameObject.Find("Result Title").GetComponent<Text>().text = "";
        GameObject.Find("Score").GetComponent<Text>().text = "";
        GameObject.Find("ReGame").GetComponent<Renderer>().enabled = false;
        GameObject.Find("Score").GetComponent<Text>().text = "점수 : " + score;
        rightObj.GetComponent<Renderer>().enabled = false;
        wrongObj.GetComponent<Renderer>().enabled = false;
    }

    // Update is called once per frame
    void Update()
    {
        GameTime.Remind -= Time.deltaTime;
        if (level != 3) GameTime.Play -= Time.deltaTime; //하드 모드가 아닐 경우

        if (bGame)
        {
            if (bComplete)
            {
                GameObject.Find("Result Title").GetComponent<Text>().text = "입력 결과";
                if (GameObject.Find("Result").GetComponent<Text>().text == GameObject.Find("Offer").GetComponent<Text>().text)
                {
                    score += 3;
                    rightObj.GetComponent<Renderer>().enabled = true;
                    Debug.Log(score);
                }
                else
                {
                    score--;
                    if (score < 0) score = 0;
                    wrongObj.GetComponent<Renderer>().enabled = true;
                }

                Thread.Sleep(3000);

                rightObj.GetComponent<Renderer>().enabled = false;
                wrongObj.GetComponent<Renderer>().enabled = false;
                Japan.Clear();
                GameObject.Find("Score").GetComponent<Text>().text = "점수 : " + score;
                resultObj.GetComponent<Text>().text = "";
                GameObject.Find("Result Title").GetComponent<Text>().text = "";
                OfferWord(level);
                bComplete = false;
            }
        }
        else if (GameTime.Remind == -1)
        {
            resultObj.GetComponent<Text>().text = "";
            GameObject.Find("Offer Title").GetComponent<Text>().text = "제시어";
            OfferWord(level);
            bGame = true;
        }

        if (GameTime.Play <= 0)
        {
            //SceneManager.LoadScene("HanseokbongResultScene");
        }
    }

    private void OfferWord(int level)
    {
        switch (level)
        {
            case 1:
                offerObj.GetComponent<Text>().text = Words.easy[(int)(Random.value * (Words.easy.Length - 1))];
                break;
            case 2:
                offerObj.GetComponent<Text>().text = Words.normal[(int)(Random.value * (Words.normal.Length - 1))];
                break;
            case 3:
                offerObj.GetComponent<Text>().text = Words.normal[(int)(Random.value * (Words.normal.Length - 1))];
                break;
            default:
                Debug.Log("레벨을 잘못 입력하셨습니다.");
                break;
        }   
    }
}

class Hangle
{
    private static char[] choSung = { 'ㄱ', 'ㄲ', 'ㄴ', 'ㄷ', 'ㄸ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅃ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅉ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ' };
    private static char[] jungSung = { 'ㅏ', 'ㅐ', 'ㅑ', 'ㅒ', 'ㅓ', 'ㅔ', 'ㅕ', 'ㅖ', 'ㅗ', 'ㅘ', 'ㅙ', 'ㅚ', 'ㅛ', 'ㅜ', 'ㅝ', 'ㅞ', 'ㅟ', 'ㅠ', 'ㅡ', 'ㅢ', 'ㅣ' };
    private static char[] jongSung = { ' ', 'ㄱ', 'ㄲ', 'ㄳ', 'ㄴ', 'ㄵ', 'ㄶ', 'ㄷ', 'ㄹ', 'ㄺ', 'ㄻ', 'ㄼ', 'ㄽ', 'ㄾ', 'ㄿ', 'ㅀ', 'ㅁ', 'ㅂ', 'ㅄ', 'ㅅ', 'ㅆ', 'ㅇ', 'ㅈ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ' };

    /*
        자음 모음 자음 3개
        자음 모음     2개
        자음 자음
        모음 모음  


        자음 모음 1글잘 조합
        자음 모음 자음 1글자 조합
        자음 모음 모음 1글자 + 모음
